package A03_Operators;

public class C03_RelationalOperators {

    public static void main(String[] args) {

        // They return only boolean values

        int a=10, b=5, result;

        // >
        System.out.println(a>b);

        // <
        System.out.println(a<b);

        // >=
        System.out.println(a>=b);

        // <=
        System.out.println(a<=b);

        // ==
        System.out.println(a==b);

        // !=
        System.out.println(a!=b);

    }
}
