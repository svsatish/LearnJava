package A03_Operators;

public class C05_LogicalOperators {

    public static void main(String[] args) {

        // They only deal with booleans

        int a=5, b=10, c=10;

        // ||
        System.out.println((a>b) || (b>=c));

        // &&
        System.out.println((a>b) && (b>=c));
    }
}
