package A03_Operators;

public class C04_AssignmentOperators {

    public static void main(String[] args) {

        // also called Shortform

        int a=10, b=5, result;

        // +=
        result = a+=b;
        System.out.println(result);

        // -=
        result = a-=b;
        System.out.println(result);

        // *=
        result = a*=b;
        System.out.println(result);

        // /=
        result = a/=b;
        System.out.println(result);

        // %=
        result = a%=b;
        System.out.println(result);

    }
}
