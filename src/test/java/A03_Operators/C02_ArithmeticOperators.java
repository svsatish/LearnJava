package A03_Operators;

public class C02_ArithmeticOperators {

    public static void main(String[] args) {

        // They can be done on any data types

        int a, b, result;

        // +
        a = 10;
        b = 5;
        result = a+b;
        System.out.println(result);

        // -
        result = a-b;
        System.out.println(result);

        // *
        result = a*b;
        System.out.println(result);

        // / - remainder
        result = a/b;
        System.out.println(result);

        // % - quotient
        result = a%b;
        System.out.println(result);

    }
}
